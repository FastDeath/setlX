package org.randoom.setlx.exceptions;

/**
 * User-generated Exception, thrown by the exit statement.
 */
public class ExitException extends SetlException {

    private static final long serialVersionUID = -6972159644509996794L;

    /**
     * Create new ExitException.
     */
    public ExitException() {
        super("Good Bye! (exit)");
    }
}

